// REPLACE 'a'
#include <iostream>
#include<algorithm>
#include <string>
using namespace std;
int main() {
    string s1, s2;
    cout << "Enter first string: ";
    getline(cin, s1);
    cout << "Enter second string: ";
    getline(cin, s2);
    int first = -1, last = -1;
    // Find first and last 'a'
    for (int i = 0; i < s1.length(); i++) {
        if (s1[i] == 'a' || s1[i] == 'A') {
            if (first == -1)
                first = i;     // first occurrence
            last = i;          // keep updating for last occurrence
        }
    }
    // Replace if found
    if (first != -1){
        s1[first] = '*';
    }
    if (last != -1){
        s1[last] = '#';
    }
    cout << "Updated 1st string: " << s1;
    return 0;
}
